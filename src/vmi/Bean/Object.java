package vmi.Bean;

public class Object {
	private int id;
	private int process_id;
	private String name;
	
	
	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setProcess_id(int process_id) {
		this.process_id = process_id;
	}

	public int getProcess_id() {
		return process_id;
	}
	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
}
